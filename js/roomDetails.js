function showMore1(){
  if(document.getElementById("hid1").style.display == "none"){
    document.getElementById("hid1").style.display = "table";
    document.getElementById('roomplus1').src = "assets/minus.png";
    document.getElementById('roomplus1').style.paddingTop = "15px";
  }
  else{
    document.getElementById("hid1").style.display = "none";
    document.getElementById('roomplus1').src = "assets/plus.png";
    document.getElementById('roomplus1').style.paddingTop = "0";
  }
}

